-- Inner Join : Joins on behalf of common records
-- Left Join : All records from left table(first table of query) & matching records from right table(else NULL filled)
-- Right Join : Vice-versa of Left Join
-- Full Outer Join : Can be Assumed as UNION of two tables
-- Self Join : 

SELECT * FROM CUSTOMERS
CROSS JOIN EMPL;

SELECT E.FIRSTNAME,S.ADDRESS
FROM STUDENT AS S
INNER JOIN EMPL AS E
ON E.EMPID=S.ROLLNO;

SELECT S.ADDRESS,e.FIRSTNAME
FROM STUDENT AS S
LEFT JOIN EMPL AS E
ON E.EMPID=S.ROLLNO;

SELECT S.ADDRESS,e.FIRSTNAME
FROM STUDENT S
RIGHT JOIN EMPL E
ON E.EMPID=S.ROLLNO;

